public class IntellijShortcutsLesson {

    /*
     *  CTRL + SPACE -> auto-completion
     *  CTRL + ENTER -> auto-solve
     *
     *  SHIFT + TAB -> move line tab back
     *  SHIFT + ARROWS -> selection
     *
     *  CTRL + SHIFT + UP/DOWN -> move selected code blocks
     *  CTRL + LEFT/RIGHT -> word move (+ SHIFT for selection)
     *
     *  CTRL + / -> comment selected lines with //
     *  CTRL + SHIFT + / -> comment selected lines with /* *\/
     *
     *  ALT + LEFT/RIGHT -> move to previous/next tab (+ CTRL for visited tabs)
     *  ALT + UP/DOWN -> move to next method in this file
     *
     *  CTRL + ALT + L -> style correction
     *  CTRL + ALT + O -> optimize imports
     *
     *  CTRL + N -> search by class name (+ SHIFT for file name)
     *
     *  CTRL + G -> go to line
     *
     *  CTRL + LMB -> go to method/field
     *  CTRL + ALT + B -> go to implementation
     *
     *  CTL + ALT + M/C/V -> extract method/constant/variable
     */
}
